void main() {
  // For Loop
  print("Using For Loop:");
  for (int i = 1; i <= 5; i++) {
    print(i);
  }

  // While Loop
  print("\nUsing While Loop:");
  int j = 1;
  while (j <= 5) {
    print(j);
    j++;
  }

  // Do-While Loop
  print("\nUsing Do-While Loop:");
  int k = 1;
  do {
    print(k);
    k++;
  } while (k <= 5);
}